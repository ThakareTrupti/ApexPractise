public class SwitchCase {
    public static void main(String[] args) {
        String i="H";
        switch(i){
            case "Hi":
                System.out.println("Hey hi");
                break;
            case "Hello":
                System.out.println("Hey Hello");
                break;
            default:
                System.out.println("Some word");
                break;

        }
    }
}
